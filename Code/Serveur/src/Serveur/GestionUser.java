    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int val_ret=-1; 
        nm=nom.getText();
        identifiant=id.getText();
        prnm=prenom.getText();
        pass=password.getText();
        String s=(String)jList.getSelectedValue();
        switch(s){
            case "groupe_enseignant":
                s = "ENSEIGNANT";
                break;
            case "groupe_informaticien":
                s = "INFORMATICIEN";
                break;
            case "groupe_administration":
                s = "ADMINISTRATION";
                break;
            case "groupe_plombier":
                s = "PLOMBIER";
                break;
            default:
                s = "ETUDIANT";
                break;
        }
        TypeUtilisateur type=TypeUtilisateur.valueOf(s);
        Utilisateur user= new Utilisateur(identifiant, nm, prnm,type);
        val_ret=database.ajouterUtilisateur(user,pass);
        if(val_ret!=1){
            JOptionPane.showMessageDialog(null, "ERROR ! \n"+nm+" "+prnm+" n'a pas été ajouté correctement.\nEssayez à nouveau.", "erreur ajout user", JOptionPane.ERROR_MESSAGE);
        }else {
            JOptionPane.showMessageDialog(null, nm+" "+prnm+" a été ajouté.\nGroupe séléctionné : "+type, "info selected", JOptionPane.INFORMATION_MESSAGE);
        }
         nm=null;
         identifiant=null;
         prnm=null;
         pass=null;
         nom.setText(null);
         prenom.setText(null);
         password.setText(null);
         id.setText(null);
    }//GEN-LAST:event_jButton1ActionPerformed

