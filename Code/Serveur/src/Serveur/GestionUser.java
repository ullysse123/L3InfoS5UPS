    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String s=(String)jList1.getSelectedValue();
        Utilisateur trouve=null;
        for(Utilisateur u : users){
            if(u.getIdentifiant().equals(s))
                trouve=u;
        }
        if(trouve==null){
            JOptionPane.showMessageDialog(null, "L'utilisateur selectionné semble ne pas avoir été trouvé...\n", "Erreur user", JOptionPane.ERROR_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "Login : "+trouve.getIdentifiant()+ "\nNom : "+trouve.getNom()+"\nPrenom : "+trouve.getPrenom(), trouve.getNom()+ " "+trouve.getPrenom(), JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

